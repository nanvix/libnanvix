#![deny(clippy::all)]
#![forbid(clippy::large_stack_frames)]
#![forbid(clippy::large_stack_arrays)]
#![feature(panic_info_message)]
#![no_std]

//==================================================================================================
// Modules
//==================================================================================================

pub mod logging;
mod panic;

//==================================================================================================
// Exports
//==================================================================================================

pub use kcall::*;

#[macro_export]
macro_rules! log{
	( $($arg:tt)* ) => ({
		use core::fmt::Write;
		use $crate::logging::Logger;
		let _ = writeln!(&mut Logger, $($arg)*);
	})
}
